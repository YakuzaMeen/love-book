// Permitir navegación con teclas de flecha
document.addEventListener('keydown', (e) => {
    if (e.key === 'ArrowRight') {
        swiper?.slideNext();
    } else if (e.key === 'ArrowLeft') {
        swiper?.slidePrev();
    }
});

