// ========================================
// FUNCIÓN: GENERAR SLIDES DINÁMICAMENTE
// ========================================

function generateSlides() {
    const wrapper = document.getElementById('swiperWrapper');
    
    // Generar slides de las 200 razones
    reasons.forEach((reason, index) => {
        const slide = document.createElement('div');
        slide.classList.add('swiper-slide');
        
        const page = document.createElement('div');
        page.classList.add('page');
        
        const pageNumber = document.createElement('div');
        pageNumber.classList.add('page-number');
        pageNumber.textContent = `Razón ${index + 1}`;
        
        const pageText = document.createElement('div');
        pageText.classList.add('page-text');
        pageText.textContent = reason;
        
        const pageHeart = document.createElement('div');
        pageHeart.classList.add('page-heart');
        pageHeart.textContent = '❤️';
        
        page.appendChild(pageNumber);
        page.appendChild(pageText);
        page.appendChild(pageHeart);
        
        slide.appendChild(page);
        wrapper.appendChild(slide);
    });
    
    // Agregar slide especial (Razón 201)
    const specialSlide = document.createElement('div');
    specialSlide.classList.add('swiper-slide');
    
    const specialPageDiv = document.createElement('div');
    specialPageDiv.classList.add('page', 'special-page');
    
    const specialPageNumber = document.createElement('div');
    specialPageNumber.classList.add('page-number');
    specialPageNumber.textContent = 'Razón 201 - La más importante';
    
    const specialPageText = document.createElement('div');
    specialPageText.classList.add('page-text');
    specialPageText.textContent = specialPage;
    
    specialPageDiv.appendChild(specialPageNumber);
    specialPageDiv.appendChild(specialPageText);
    
    specialSlide.appendChild(specialPageDiv);
    wrapper.appendChild(specialSlide);
    
    // Agregar slide de propuesta
    const proposalSlide = document.createElement('div');
    proposalSlide.classList.add('swiper-slide');
    
    const proposalPageDiv = document.createElement('div');
    proposalPageDiv.classList.add('page', 'proposal-page');
    
    const proposalPageText = document.createElement('div');
    proposalPageText.classList.add('page-text');
    proposalPageText.innerHTML = proposalText.replace(/\n/g, '<br>');
    
    const proposalButtonsDiv = document.createElement('div');
    proposalButtonsDiv.classList.add('proposal-buttons');
    
    // Botón 1: Sí
    const btn1 = document.createElement('button');
    btn1.classList.add('proposal-btn');
    btn1.textContent = 'Sí 💖';
    btn1.addEventListener('click', () => showProposalResponse());
    
    // Botón 2: Obvio que sí
    const btn2 = document.createElement('button');
    btn2.classList.add('proposal-btn');
    btn2.textContent = 'Obvio que sí 💕';
    btn2.addEventListener('click', () => showProposalResponse());
    
    proposalButtonsDiv.appendChild(btn1);
    proposalButtonsDiv.appendChild(btn2);
    
    proposalPageDiv.appendChild(proposalPageText);
    proposalPageDiv.appendChild(proposalButtonsDiv);
    
    proposalSlide.appendChild(proposalPageDiv);
    wrapper.appendChild(proposalSlide);
}

// ========================================
// FUNCIÓN: INICIALIZAR SWIPER
// ========================================

function initializeSwiper() {
    swiper = new Swiper('.swiper-container', {
        direction: 'horizontal',
        loop: false,
        pagination: {
            el: '.swiper-pagination',
            clickable: true,
            dynamicBullets: true
        },
        navigation: {
            nextEl: '.swiper-button-next',
            prevEl: '.swiper-button-prev'
        },
        slidesPerView: 1,
        spaceBetween: 0,
        speed: 500,
        effect: 'slide',
        keyboard: {
            enabled: true,
            onlyInViewport: false
        },
        touchRatio: 1,
        grabCursor: true
    });
}

// ========================================
// FUNCIÓN: MOSTRAR RESPUESTA DE PROPUESTA
// ========================================

function showProposalResponse() {
    const modal = document.getElementById('proposalModal');
    const modalText = document.getElementById('modalText');
    
    // Mostrar modal
    modal.classList.remove('hidden');
    
    // Mostrar texto de respuesta
    modalText.textContent = 'Ahora oficialmente eres mi novia ❤️';
    
    // Crear animación de corazones flotantes
    createFloatingHearts();
    
    // Cerrar modal después de 4 segundos
    setTimeout(() => {
        modal.classList.add('hidden');
    }, 4000);
}

// ========================================
// FUNCIÓN: CREAR CORAZONES FLOTANTES
// ========================================

function createFloatingHearts() {
    const heartsContainer = document.getElementById('floatingHearts');
    
    // Limpiar corazones anteriores
    heartsContainer.innerHTML = '';
    
    // Crear múltiples corazones flotantes
    for (let i = 0; i < 15; i++) {
        const heart = document.createElement('div');
        heart.classList.add('floating-heart');
        heart.textContent = '❤️';
        
        // Posición aleatoria horizontal
        const leftPosition = Math.random() * 100;
        heart.style.left = leftPosition + '%';
        
        // Traslación horizontal aleatoria
        const translateX = (Math.random() - 0.5) * 200;
        heart.style.setProperty('--tx', translateX + 'px');
        
        // Duración ligeramente aleatoria
        const duration = 2 + Math.random() * 1;
        heart.style.animation = `float-up ${duration}s ease-in forwards`;
        
        // Delay aleatorio
        const delay = Math.random() * 0.5;
        heart.style.animationDelay = delay + 's';
        
        heartsContainer.appendChild(heart);
    }
}

