    // INICIALIZAR SWIPER con efecto flip (simula pasar hoja) o slide en iOS
    function initializeSwiper() {
        const iOS = isIOS();
        const useFlipEffect = !iOS; // Usar flip solo si no es iOS
        
        const baseConfig = {
            direction: 'horizontal',
            loop: false,
            slidesPerView: 1,
            spaceBetween: 0,
            touchRatio: 1,
            grabCursor: !iOS
        };

        if (iOS) {
            // Config minimalista para iOS
            Object.assign(baseConfig, {
                effect: 'slide',
                speed: 200,
                simulateTouch: true,
                touchEventsTarget: 'container',
                allowMultipleTouches: false,
                threshold: 5,
                freeMode: false,
                freeModeSticky: false,
                preloadImages: false,
                lazy: {
                    loadPrevNext: false
                }
            });
        } else {
            // Config completa para Desktop/Android
            Object.assign(baseConfig, {
                effect: 'flip',
                flipEffect: {
                    slideShadows: false,
                    limitRotation: true
                },
                pagination: {
                    el: '.swiper-pagination',
                    clickable: true,
                    dynamicBullets: true
                },
                navigation: {
                    nextEl: '.swiper-button-next',
                    prevEl: '.swiper-button-prev'
                },
                speed: 600,
                keyboard: {
                    enabled: true,
                    onlyInViewport: false
                }
            });
        }

        swiper = new Swiper('.swiper-container', baseConfig);
    }

    // FLUJO DE CONFIRMACIONES ANIDADAS PARA LA PROPUESTA
    function startProposalFlow() {
        const confirmModal = document.getElementById('confirmModal');
        const confirmModal2 = document.getElementById('confirmModal2');
        const finalWarnModal = document.getElementById('finalWarnModal');

        confirmModal.classList.remove('hidden');

        document.getElementById('confirmYes').onclick = () => {
            confirmModal.classList.add('hidden');
            confirmModal2.classList.remove('hidden');
        };
        document.getElementById('confirmNo').onclick = () => {
            confirmModal.classList.add('hidden');
        };

        document.getElementById('confirmYes2').onclick = () => {
            confirmModal2.classList.add('hidden');
            finalWarnModal.classList.remove('hidden');
        };
        document.getElementById('confirmNo2').onclick = () => {
            confirmModal2.classList.add('hidden');
        };

        document.getElementById('continueBtn').onclick = () => {
            finalWarnModal.classList.add('hidden');
            revealProposalContent();
        };
    }

    // REVELAR LA DECLARACIÓN Y BOTONES (después de confirmaciones)
    function revealProposalContent() {
        const hiddenContents = document.querySelectorAll('.hidden-content');
        hiddenContents.forEach(content => {
            content.classList.remove('hidden-content');
            content.classList.add('revealed');
        });
        // una vez revelado, ya no es necesaria la instrucción
        const instr = document.querySelector('.instruction-message');
        if (instr) {
            instr.style.display = 'none';
        }
    }

    // MOSTRAR MENSAJE FINAL Y CORAZONES (cuando se presiona Sí o Obvio que sí)
    function showProposalResponse() {
        const modal = document.getElementById('proposalModal');
        const modalText = document.getElementById('modalText');
        modalText.textContent = 'Oficialmente eres mi novia';
        modal.classList.remove('hidden');
        createFloatingHearts();
        setTimeout(() => modal.classList.add('hidden'), 4200);
    }

    // CORAZONES FLOTANTES (solo en Desktop/Android)
    function createFloatingHearts() {
        if (isIOS()) return; // No crear corazones en iOS
        
        const heartsContainer = document.getElementById('floatingHearts');
        heartsContainer.innerHTML = '';
        for (let i = 0; i < 14; i++) {
            const heart = document.createElement('div');
            heart.className = 'floating-heart';
            heart.textContent = '❤️';
            heart.style.left = Math.random() * 100 + '%';
            heart.style.setProperty('--tx', ((Math.random() - 0.5) * 200) + 'px');
            const duration = 2 + Math.random() * 1.5;
            heart.style.animation = `float-up ${duration}s ease-in forwards`;
            heart.style.animationDelay = (Math.random() * 0.6) + 's';
            heartsContainer.appendChild(heart);
        }
    }

    // NAVEGACIÓN CON TECLAS
    document.addEventListener('keydown', (e) => {
        if (!swiper) return;
        if (e.key === 'ArrowRight') swiper.slideNext();
        if (e.key === 'ArrowLeft') swiper.slidePrev();
    });

    // NAVEGACIÓN RÁPIDA A PÁGINA ESPECÍFICA
    function setupQuickNav() {
        if (isIOS()) return; // Deshabilitar quick nav en iOS
        
        const pageInput = document.getElementById('pageInput');
        const pageBtn = document.getElementById('pageBtn');

        const goToPage = () => {
            const pageNum = parseInt(pageInput.value);
            const totalSlides = 1 + reasons.length + 1 + 1; // portada + 200 razones + razón 201 + propuesta

            if (pageNum < 1 || pageNum > totalSlides || isNaN(pageNum)) {
                alert(`Por favor ingresa un número entre 1 y ${totalSlides}`);
                return;
            }

            // Restar 1 porque los índices comienzan en 0
            swiper.slideTo(pageNum - 1, 600);
            pageInput.value = '';
            pageInput.blur();
        };

        pageBtn.addEventListener('click', goToPage);
        pageInput.addEventListener('keypress', (e) => {
            if (e.key === 'Enter') goToPage();
        });
    }

